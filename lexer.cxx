#include <stack>
#include "lexer.hxx"

#define START 0
#define DEAD  27

Token Lexer::next_token()
{
S:
    // 如果已经读到文件结尾
    // 则返回一个文件结束的 Token
    if (at_eof())
    {
        return Token(TokenType::END_OF_FILE, "", line_);
    }

    std::stack<int> status_stack;
    status_stack.push(status_);

    // 当 DFA 状态是 DEAD
    // 或者到达文件结尾
    // 就跳出循环
    char c;
    while (status_ != DEAD && (c = next_char()) != EOF)
    {
        // 如果输入的字符不在合法字符集内
        if (!is_valid_char(c))
        {
            reset();
            return Token(TokenType::ERROR, std::string(1, c), line_);
        }

        status_ = DFAtran_.at(status_).at(c);
        status_stack.push(status_);
    }

    // 从栈中弹出状态
    // 直到找到一个终止状态
    while (status_stack.top() != START)
    {
        status_ = status_stack.top();
        status_stack.pop();

        // 如果当前状态是终止状态
        // 则返回 Token
        if (is_final_state())
        {
            // 如果当前状态是空白状态
            // 则跳过空白状态
            // 重新开始 DFA
            if (is_ws_state())
            {
                // 恢复 DFA 的初始状态
                reset();
                goto S;
            }

            TokenType   token_type = get_token_type();
            std::string lexeme     = source_.substr(lexeme_begin_, lexeme_end_ - lexeme_begin_);

            // 恢复 DFA 的初始状态
            reset();
            return Token(token_type, lexeme, line_);
        }

        // 如果当前状态不是终止状态
        // 则继续向前寻找
        // 直到找到一个终止状态
        prev_position();
    }

    // 如果没有找到终止状态
    // 则返回一个错误 Token
    // 使用恐慌模式处理错误
    // 直接跳过当前字符
    ++lexeme_end_;
    std::string lexeme = get_lexeme();

    // 恢复 DFA 的初始状态
    reset();
    return Token(TokenType::ERROR, lexeme, line_);
}

char Lexer::next_char()
{
    if (at_eof())
        return EOF;
    char c = source_[lexeme_end_++];
    if (c == '\n')
        ++line_;
    return c;
}

void Lexer::prev_position()
{
    char c = source_[--lexeme_end_];
    if (c == '\n')
        --line_;
}

std::string Lexer::get_lexeme() const
{
    return source_.substr(lexeme_begin_, lexeme_end_ - lexeme_begin_);
}

bool Lexer::is_valid_char(char c)
{
    return alphabet_.contains(c);
}

bool Lexer::at_eof() const
{
    return lexeme_end_ == source_.size();
}

bool Lexer::is_final_state() const
{
    return status_ != START && status_ != DEAD && status_ != 25 && status_ != 26;
}

bool Lexer::is_ws_state() const
{
    return status_ == 9;
}

// 根据终止状态返回对应的 TokenType
TokenType Lexer::get_token_type() const
{
    switch (status_)
    {
        case 48 : return TokenType::FUNC;
        case 46 : return TokenType::VAR;
        case 31 : return TokenType::IF;
        case 49 : return TokenType::GOTO;
        case 52 : return TokenType::RETURN;
        case 44 : return TokenType::INT;
        case 47 : return TokenType::BOOL;
        case 1 :
        case 2 :
        case 3 :
        case 4 :
        case 5 :
        case 6 :
        case 7 :
        case 28 :
        case 29 :
        case 30 :
        case 32 :
        case 33 :
        case 34 :
        case 41 :
        case 42 :
        case 43 :
        case 45 :
        case 50 :
        case 51 :
            return TokenType::ID;
        case 8  : return TokenType::INT_LITERAL;
        case 17 : return TokenType::ADD;
        case 18 : return TokenType::SUB;
        case 19 : return TokenType::MUL;
        case 20 : return TokenType::DIV;
        case 35 : return TokenType::EQ;
        case 36 : return TokenType::NEQ;
        case 23 : return TokenType::LT;
        case 24 : return TokenType::GT;
        case 37 : return TokenType::LE;
        case 38 : return TokenType::GE;
        case 40 : return TokenType::AND;
        case 39 : return TokenType::OR;
        case 22 : return TokenType::NOT;
        case 21 : return TokenType::ASSIGN;
        case 10 : return TokenType::COMMA;
        case 11 : return TokenType::SEMICOLON;
        case 12 : return TokenType::LPAREN;
        case 13 : return TokenType::RPAREN;
        case 14 : return TokenType::LBRACE;
        case 15 : return TokenType::RBRACE;
        case 16 : return TokenType::COLON;
        default : return TokenType::ERROR;
    }
}

void Lexer::reset()
{
    status_       = START;
    lexeme_begin_ = lexeme_end_;
}

